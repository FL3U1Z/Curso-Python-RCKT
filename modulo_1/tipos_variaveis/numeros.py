#inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)

#real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

#tipo da variável
print("tipo da variavel numero_real = ", type(numero_real))

#Soma +
soma = 2 + 2
print("2 + 2 = ", soma)

#Subtração - 
sub = 10 - 3
print("10 - 3 = ", sub)

#Multiplicação
multi = 5 * 5
print("5 * 5 = ", multi)

#Divisão
divisao = 5/2
print("5/2 = ", divisao)
print("tipo da variavel da divisão= ", type(divisao))

#Divisão inteiro //
divisao = 5 // 2
print("5/2 = ", divisao)
print("tipo da variavel da divisão em inteiro = ", type(divisao))

#int()
print("Valor em inteiro = ", int(divisao))

#float()
print("Valor em float = ", float(divisao))

#modulo
modulo = 5 % 2
print("Modulo = ", modulo)